class DataStorage {
  private data: string[] | number[] | boolean[] = [];

  // Uniton Type Problem: Union Types except either of the defined types 
  // everytime the method gets called.  However, data only has one defined type
  addItem(item: string | number | boolean) {
    this.data.push(item);
  }

  removeItem(item: string | number | boolean) {
    if (this.data.indexOf(item) === -1) {
      return;
    }
    this.data.splice(this.data.indexOf(item), 1); // -1
  }

  getItems() {
    return [...this.data];
  }
}

